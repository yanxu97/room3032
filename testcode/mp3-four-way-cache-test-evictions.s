.align 4
.section .text
.globl _start

_start:

    # Trying to just test load word and store word? Use the MP0 tests.
	  # Trying to test storage and cache reads without writeback? Try the initial load and store test suite given in MP2 CP2.

    # This test is written to be deliberately compact, instruction wise.
	  # Our code should span no more than a few lines to load data and store
	  # data, but to do so on one specific line of the cache at all times.
    # The instructions themselves can span multiple cache lines, but the data
    # should be always at one specific line that can get switched out depending on
    # what was most recently used.

    # This will write "good" to all five sets of data, and eventually evict all four ways, but outside of a 1,2,3,4 pattern.


    lw x11, one
    lw x12, two
    lw x13, three
    lw x14, four
    lw x15, five
    la x21, setone
    la x22, settwo
    la x23, setthree
    la x24, setfour
    la x25, setfive

# 1, 3, 2, write 3, 4, read 1, read 5 (E2), write 4, read 2 (E3 with writeback), write 1, write 2, write 5.

test_one:
    lw x1, setone       # 1, X, X, X
    lw x2, settwo       # 2, 1, X, X
    lw x3, setthree     # 3, 2, 1, X
    sw x11, 0(x21)       # 1, 3, 2, X is pattern after writing 1111 to dataset 1 (in way 1)
    lw x4, setfour      # 4, 1, 3, 2
    lw x5, setfive      # 5, 4, 1, 3: EVICT Way 2.
    sw x13, 0(x23)       # 3, 5, 4, 1: write 3333 to dataset 3

    # To make it clear that what data loads are what, clearing registers.
    add x1, x0, x0
    add x2, x0, x0
    add x3, x0, x0
    add x4, x0, x0
    add x5, x0, x0

    lw x2, settwo       # 2, 3, 5, 4: EVICT Way 1. WRITE BACK data set 1.
    sw x15, 0(x25)      # 5, 2, 3, 4: write 5555 to dataset 5
    sw x14, 0(x24)      # 4, 5, 2, 3: write 4444 to dataset 4
    lw x1, setone       # 1, 4, 5, 2: EVICT Way 3. WRITE BACK data set 3.
    sw x12, 0(x22)      # 2, 1, 4, 5: write 2222 to dataset 2, all memory spaces written to at this point
    lw x3, setthree     # 3, 2, 1, 4: EVICT Way 2. WRITE BACK data set 5.
    lw x5, setfive      # 5, 3, 2, 1: EVICT Way 4. WRITE BACK data set 4. Completes all ways evicted.
    lw x4, setfour      # 4, 5, 3, 2
    add x1, x0, x0
    lw x1, setone       # 1, 4, 5, 3: WRITE BACK data set 2. Completes all write backs.
    lw x2, settwo
    lw x3, setthree
    lw x4, setfour
    lw x5, setfive

    # Result should show 1111, 2222, 3333, 4444, 5555 in x1 to x5.

# A bit ironic that we are closing with an unconditional jump itself, but this does indeed happen.
goodend:
    beq x0, x0, goodend

